◊سرخی{سخن ہائے گفتنی}

اپریل ◊سال{۲۰۰۶}ء میں دیوانِ غالبؔ پر جو کام اردو محفل پر شروع ہوا تھا،آج اکتوبر ◊سال{۲۰۱۹}ء میں کُلّیاتِ غالبؔ کی شکل میں آپ کے سامنے ہے۔
 
ان ۱۳ سالوں میں دیوانِ غالبؔ پر جو کام ہوا، اس کی تفصیل یہ ہے:

◊عددی-فہرست{
یکم اپریل ◊سال{۲۰۰۶}ء کو اس کی ٹائپنگ اور محفل پر پوسٹنگ مکمل ہو گئی۔ 



اپریل ◊سال{۲۰۰۶}ء میں ہی اعجاز عبید صاحب نے سارے کام کو ایک فائل میں مرتب کیا، اس کی ابتدائی پروف‌رِیڈنگ کی اور اس کا دیباچہ لکھا۔ نیز مندرجہ ذیل نسخوں کے ساتھ موازنہ کیا اور ان نسخوں کے اختلاف پر فُٹ‌نوٹس لکھے۔ 
