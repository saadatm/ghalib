}



مئی ◊سال{۲۰۰۶}ء نے اردو محفل کے ارکان کے اصرار پر میں نے دیوان پر کام شروع کیا۔ ۲۰ جون کو میں نے اس کی پروف ریڈنگ مکمل کی اور مندرجہ ذیل نسخوں کے ساتھ موازنہ کیا اور ان نسخوں کے اختلاف پر فُٹ‌نوٹس لکھے۔ 
◊عددی-فہرست{
نوائے سروش از مولانا غلام رسول مہر (نسخۂ مہر)



شرحِ دیوانِ غالبؔ از علامہ عبد الباری آسی (نسخۂ آسی)



