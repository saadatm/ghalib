جس کو کہتے ہیں جمائی ◊خک{فازہ} ہے
جو ہے انگڑائی، وہی ◊خک{خمیازہ} ہے

◊خک{بارہ} کہتے ہیں کڑے کو، ہم سے پوچھ
پاڑ ہے ◊خک{تالار}، اک عالم سے پوچھ

جس طرح گہنے کی ◊خک{زیور} فارسی
اسی طرح ہنسلی کی ◊خک{پرگر} فارسی

بھِڑ کی بھائی، فارسی ◊خک{زنبور} ہے
دسپنا ◊خک{اُنبر} ہے اور ◊خک{انبور} ہے

فارسی ◊خک{آئینہ}، ہندی آرسی
اور ہے کنگھے کی ◊خک{شانہ} فارسی

ہینگ ◊خک{انگوزہ} ہے اور ◊خک{ارزیر} رانگ
◊خک{ساز} باجا اور ہے آواز ◊خک{بانگ}

◊خک{زوجہ} جورو، ◊خک{یزنہ} بہنوئی کو جان
◊خک{خشم} غصے اور بدخوئی کو جان

لوہے کو کہتے ہیں ◊خک{آہن} اور ◊خک{حدید}
جو نئی ہے چیز اسے کہیے ◊خک{جدید}

ہے ◊خک{نوا} آواز، ساماں اور اُول
◊خک{نرخ}، قیمت اور ◊خک{بہا}، یہ سب ہیں مول

◊خک{سیر} لہسن، ◊خک{تُرب} مولی، ◊خک{ترہ} ساگ
کھا ◊خک{بخور}، ◊خک{برخیز} اُٹھ، ◊خک{بگریز} بھاگ

روئی کی پونی کا ہے ◊خک{پاغُند} نام
◊خک{دُوک} تکلے کو کہیں گے، لا کلام

◊خک{گیتی} اور ◊خک{گیہاں} ہے دنیا یاد رکھ
اور ہے ◊خک{ندّاف}، دھُنیا یاد رکھ

◊خک{کوہ} کو ہندی میں کہتے ہیں پہاڑ
فارسی ◊خک{گلخن} ہے اور ہندی ہے بھاڑ

تکیہ ◊خک{بالش} اور بچھونا بسترا
اصل ◊خک{بستر} ہے سمجھ لو تم ذرا

بسترا بولیں سپاہی اور فقیر
ورنہ ◊خک{بستر} کہتے ہیں برنا و پیر

◊خک{پیر} بوڑھا اور ◊خک{برنا} ہے جواں
جان کو البتہ کہتے ہیں ◊خک{رواں}

اینٹ کے گارے کا نام ◊خک{آژند} ہے
ہے نصیحت بھی وہی جو ◊خک{پند} ہے

پند کو ◊خک{اندرز} بھی کہتے ہیں، ہاں
◊خک{ارض} ہے، پر، ◊خک{مرز} بھی کہتے ہیں ہاں

کیا ہے ◊خک{ارض} اور ◊خک{مرز} تم سمجھے؟ زمیں
◊خک{عنق} گردن اور پیشانی ◊خک{جبیں}

◊خک{آس} چکّی، ◊خک{آسیا} مشہور ہے
اور ◊خک{فَوفل} چھالیا مشہور ہے

بانسلی ◊خک{نے} اور ◊خک{جلاجل} جانجھ ہے
پھر ◊خک{ستَروَن} اور ◊خک{عقیمہ} بانجھ ہے

◊خک{کُحل} سرمہ اور سلائی ◊خک{مِیل} ہے
جس کو جھولی کہیے، وہ ◊خک{زنبیل} ہے

پایا قادر نامے نے آج اختتام
اک غزل اور پڑھ لو، والسّلام!

◊شاعری-میں-سرخی{غزل}

شعر کے پڑھنے میں کچھ حاصل نہیں
مانتا لیکن ہمارا دل، نہیں

علم ہی سے قدر ہے انسان کی
ہے وہی انسان جو جاہل نہیں

کیا کہِیں کھائی ہے حافظ جی کی مار؟
آج ہنستے آپ جو کھِل کھِل نہیں

کس طرح پڑھتے ہو رک رک کر سبق؟
ایسے پڑھنے کا تو میں قائل نہیں

جس نے قادر نامہ سارا پڑھ لیا
اس کو آمد نامہ کچھ مشکل نہیں
}

